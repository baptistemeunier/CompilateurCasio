		Fonctionne avec l'élément "Go To", lorsque le programme 
		lit l'élément "Go To", il va directement au label 
		correspondant.
	</div>
	<div id="aideGoTo">
		Fonctionne avec l'élément "Label", lorsque le programme 
		lit cette instruction, il va directement au "label" 
		correspondant.
	</div>
	<div id="aideSet">
		Permet de régler la calculette pour faire des calculs en 
		radian ou en degrée.
	</div>
