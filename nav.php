		Permet d'afficher le texte voulu.
	</div>
	<div id="aideCalcul">
		Permet d'associer à une variable le calcul de variable(s)
		souhaitée(s).
	</div>
	<div id="aideLire">
		Permet de lire la variable souhaitée à l'utilisateur du 
		programme.
	</div>
	<div id="aideIf">
		Permet d'éxécuter les instructions voulu à certaines 
		condiction données.
	</div>
	<div id="aideWhile">
		Permet d'éxécuter les instructions voulu un nombre de 
		fois voulu donnée sous forme de condition.
	</div>
	<div id="aideFor">
		Peremet d'éxécuter les instructions voulu un nombre de 
		fois voulu en indiquant un nombre précis de fois.
